import math
print('====== DESAFIO 18 ======')
angulo = int(input('Digite o valor do ângulo: '))
seno = math.sin(angulo)
cosseno = math.cos(angulo)
tangente = math.tan(angulo)
print('O valor do Seno é: {:.11f}'.format(seno))
print('O valor do Cosseno é: {:.11f}'.format(cosseno))
print('O valor da Tangente é: {:.11f}'.format(tangente))
